        print_warning "â€¢ Swap Service: Port $SWAP_SERVICE_LOCAL_PORT - Executes the swap using Pyth price feeds"
    fi
    echo ""
    
    local agents_pid=$(start_agent_servers)
    sleep 3
    check_agents_status
    
    # Display service information
    display_endpoints "$environment"
    
    echo ""
    print_info "ðŸ’± Swap Demo Features:"
    print_color "$CYAN" "  â€¢ Real-time SOL/USD pricing from Pyth Network"
    print_color "$CYAN" "  â€¢ USDC payments via ACK Lab SDK"
    print_color "$CYAN" "  â€¢ Simulated DEX swap execution"
    print_color "$CYAN" "  â€¢ SOL delivery to the wallet"
    
    # Main interaction loop
    echo ""
    display_menu
    
    while true; do
        read -p "$(print_color "$CYAN" "Enter your choice (1/2/3): ")" choice
        
        case $choice in
            1)
                run_tutorial
                display_post_action_menu "Tutorial"
                ;;
            2)
                run_cli_demo
                display_post_action_menu "CLI Demo"
                ;;
            3)
                cleanup_and_exit "$agents_pid"
                ;;
            *)
                print_error "Invalid choice. Please enter 1, 2, or 3."
                ;;
        esac
    done
}

# Run main function
main
