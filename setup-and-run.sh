# ==================== Main Execution ====================
main() {
    # Initial setup
    print_header "ðŸ’± USDC to SOL Swap Demo Setup"
    
    # Setup environment
    ensure_env_file
    load_env
    
    echo ""
    print_info "ðŸ”§ Checking required credentials..."
    
