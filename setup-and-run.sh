            print_warning "Please add the missing credentials using the Replit Secrets tool and run this script again."
            print_info "Click the ðŸ”’ Secrets icon in the Tools panel to add your credentials securely."
        else
            print_warning "Please add the missing credentials to your .env file and run this script again."
        fi
        exit 1
    fi
    
    # Install dependencies
    echo ""
    install_dependencies
    
    # Detect environment
    echo ""
    local environment=$(detect_environment)
    
    # Start services
    echo ""
    print_info "ðŸŽ¯ Starting USDC to SOL Swap Agents Server..."
    print_warning "This will start two swap agent servers:"
    
    if [ "$environment" = "replit" ]; then
        print_warning "â€¢ Swap User: Port $SWAP_USER_LOCAL_PORT (accessible via port 3000) - Wants to swap USDC for SOL"
        print_warning "â€¢ Swap Service: Port $SWAP_SERVICE_LOCAL_PORT (accessible via port 3001) - Executes the swap using Pyth price feeds"
